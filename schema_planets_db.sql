
DROP DATABASE IF EXISTS climazon_db;
CREATE DATABASE climazon_db;
USE climazon_db;

CREATE TABLE planets (
    rowid INTEGER(4) NOT NULL,
    fpl_hostname VARCHAR(255),
    fpl_letter VARCHAR(1),
    fpl_name VARCHAR(255),
    fpl_discmethod VARCHAR(255),
    fpl_disc YEAR,
    fpl_orbper DECIMAL(20,10),
    fpl_orbperreflink VARCHAR(255),
    fpl_smax DECIMAL(20,10),
    fpl_smaxreflink VARCHAR(255),
    fpl_eccen DECIMAL(20,10),
    fpl_eccenreflink VARCHAR(255),
    fpl_bmasse DECIMAL(20,10),
    fpl_bmassj DECIMAL(20,10),
    fpl_bmassprov VARCHAR(255),
    fpl_bmassreflink VARCHAR(255),
    fpl_rade DECIMAL(20,10),
    fpl_radj DECIMAL(20,10),
    fpl_rads DECIMAL(20,10),
    fpl_radreflink VARCHAR(255),
    fpl_dens DECIMAL(20,10),
    fpl_densreflink VARCHAR(255),
    fpl_eqt DECIMAL(20,10),
    fpl_eqtreflink VARCHAR(255),
    fpl_insol DECIMAL(20,10),
    fpl_insolreflink VARCHAR(255),
    fpl_tranflag DECIMAL(20,10),
    fpl_cbflag DECIMAL(20,10),
    fpl_snum DECIMAL(20,10),
    ra_str VARCHAR(255),
    ra DECIMAL(20,10),
    dec_str VARCHAR(255),
    dec_ DECIMAL(20,10),
    fst_posreflink VARCHAR(255),
    fst_dist DECIMAL(20,10),
    fst_distreflink VARCHAR(255),
    fst_optmag DECIMAL(20,10),
    fst_optmagband VARCHAR(255),
    fst_optmagreflink VARCHAR(255),
    fst_nirmag DECIMAL(20,10),
    fst_nirmagband VARCHAR(255),
    fst_nirmagreflink VARCHAR(255),
    fst_spt VARCHAR(255),
    fst_sptreflink VARCHAR(255),
    fst_teff DECIMAL(20,10),
    fst_teffreflink VARCHAR(255),
    fst_logg DECIMAL(20,10),
    fst_loggreflink VARCHAR(255),
    fst_lum DECIMAL(20,10),
    fst_lumreflink VARCHAR(255),
    fst_mass DECIMAL(20,10),
    fst_massreflink VARCHAR(255),
    fst_rad DECIMAL(20,10),
    fst_radreflink VARCHAR(255),
    fst_met DECIMAL(20,10),
    fst_metratio VARCHAR(255),
    fst_metreflink VARCHAR(255),
    fst_age DECIMAL(20,10),
    fst_agereflink VARCHAR(255),
    PRIMARY KEY (rowid)
);

